let currentIndex = 0;
let isRevealed = false;

function displayWord() {
    isRevealed = false;
    const word = Math.random() < 0.5 ? wordPairs[currentIndex].eng : wordPairs[currentIndex].kor;
    document.getElementById('word').innerText = word;
    document.getElementById('progress').innerText = `${currentIndex + 1} / ${wordPairs.length}`;
}

function revealWord() {
    if (!isRevealed) {
        document.getElementById('word').innerText = `${wordPairs[currentIndex].eng} - ${wordPairs[currentIndex].kor}`;
        isRevealed = true;
    } else {
        nextWord();
    }
}

function nextWord() {
    if (currentIndex < wordPairs.length - 1) {
        currentIndex++;
        displayWord();
    } else {
        window.location.href = "3.html";
    }
}

function previousWord() {
    if (currentIndex > 0) {
        currentIndex--;
        displayWord();
    }
}

